#import "UIColor+Extensions.h"

@implementation UIColor (Extensions)

+ (UIColor *)noteYellow
{
    return [UIColor colorWithRed:0.923 green:0.916 blue:0.460 alpha:1.000];
}

+ (UIColor *)noteRed
{
    return [UIColor colorWithRed:0.923 green:0.710 blue:0.541 alpha:1.000];
}

+ (UIColor *)noteBlue
{
    return [UIColor colorWithRed:0.755 green:0.844 blue:0.923 alpha:1.000];
}

+ (UIColor *)noteGreen
{
    return [UIColor colorWithRed:0.643 green:0.924 blue:0.585 alpha:1.000];
}

@end
